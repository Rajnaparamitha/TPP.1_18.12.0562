   <div class="col-12">
      <h3 class="font-weight-bold" style="color: #27AAC6;"><span class="fa fa-heart fa-1x"></span>&ensp;Stimulasi</h3>
      <dd class="font-weight-bold ml-5">Kemampuan gerak kasar</dd>
      <ul class="ml-3" style="list-style-type: inherit;">
         <li> Mengangkat kepala</li>
         <li> Berguling-guling</li>
         <li> Menahan kepala tetap tegak</li>
      </ul>
      <dd class="font-weight-bold ml-5">Kemampuan gerak halus</dd>
      <ul class="ml-3" style="list-style-type: inherit;">
         <li> Melihat, meraih dan menendang mainan gantung</li>
         <li> Memperhatikan benda bergerak</li>
         <li> Melihat benda-benda kecil</li>
         <li> Memegang benda</li>
         <li> Meraba dan merasakan bentuk permukaan</li>
      </ul>
      <dd class="font-weight-bold ml-5"> Kemampuan Bicara dan Bahasa</dd>
      <ul class="ml-3" style="list-style-type: inherit;">
         <li> Berbicara</li>
         <li> Meniru suara-suara</li>
         <li> Mengenali berbagai suara</li>

      </ul>
      <dd class="font-weight-bold ml-5"> Kemampuan Sosialisasi dan Kemandirian</dd>
      <ul class="ml-3" style="list-style-type: inherit;">
         <li> Memberi rasa aman dan kasih sayang</li>
         <li> Mengajak bayi tersenyum</li>
         <li> Mengajak bayi mengamati benda-benda dan keadaan disekitarnya</li>
         <li> Meniru ocehan dan mimik muka bayi</li>
         <li> Mengayun bayi</li>
         <li> Menina-bobokan</li>
      </ul>
