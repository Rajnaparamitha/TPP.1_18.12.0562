   <div class="col-12">
      <h3 class="font-weight-bold" style="color: #27AAC6;"><span class="fa fa-plus-square fa-1x"></span>&ensp;Kebutuhan Gizi</h3>
      <dd class="ml-5">Kebutuhan gizi pada bayi usia 0-6 bulan cukup terpenuhi dari ASI saja (ASI Eksklusif)</dd>
      <ul class="ml-3" style="list-style-type: inherit;">
         <li> Berikan ASI yang pertama keluar dan berwarna kekuningan (kolostrum)</li>
         <li> Jangan berikan makanan atau minuman selain ASI</li>
         <li> Susui bayi sesering mungkin</li>
         <li> Susui setiap bayi menginginkan, paling sedikit 8 kali sehari</li>
         <li> Jika bayi tidur lebih dari 3 jam, bangunkan lalu susui</li>
         <li> Susui dengan payudara kanan dan kiri secara bergantian</li>
         <li> Susui sampai payudara terasa kosong, lalu pindah ke payudara sisi lainnya</li>
      </ul>
      <hr>
   </div>
   <div class="col-12">
      <h3 class="font-weight-bold" style="color: #27AAC6;"><span class="fa fa-child fa-1x"></span>&ensp;Tahap Perkembangan</h3>
      <ul class="ml-3" style="list-style-type: inherit;">
         <li>Mengangkat kepala setinggi 45 derajat</li>
         <li>Menggerakkan kepala dari kiri/kanan ke tengah.</li>
         <li>Melihat dan menatap wajah anda.</li>
         <li>Mengoceh spontan atau bereaksi dengan mengoceh.</li>
         <li>Suka tertawa keras.</li>
         <li>Bereaksi terkejut terhadap suara keras.</li>
         <li>Membalas tersenyum ketika diajak bicara/tersenyum.</li>
         <li>Mengenal ibu dengan penglihatan, penciuman, pendengaran, kontak</li>
      </ul>
      <hr>
